package org.entur.logbook.example.rest;

public class MyEntity {
	
	private String secret;
	private String name;
	
	public String getSecret() {
		return secret;
	}
	public void setSecret(String secret) {
		this.secret = secret;
	}
	public String getName() {
		return name;
	}
	public void setName(String key) {
		this.name = key;
	}


	
}
